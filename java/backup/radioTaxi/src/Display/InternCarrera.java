    private void btnIngresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIngresarActionPerformed
        RadioTaxiDAL rd = new RadioTaxiDAL();
        try {
            if (rd.existeCliente(this.txtRutCliente.getText().trim())) {
                Carrera race = new Carrera();
                race.setDire_destino(this.txtDestino.getText());
                race.setDire_inicio(this.txtBusqueda.getText());
                race.setFecha(this.txtFecha.getText());
                race.setHora_salida(this.txtHoraSalida.getText());
                race.setPatente_taxi(this.cmbDisponibles.getSelectedItem().toString());
                race.setRut_cliente(this.txtRutCliente.getText());
                if (rd.saveCarrera(race)) {
                    JOptionPane.showMessageDialog(this, "Se guardo CARRERA");
                    Taxi tx = new Taxi();
                    tx.setEstado('C');
                    tx.setPatente(this.cmbDisponibles.getSelectedItem().toString());
                    if (rd.updateTaxi(tx)) {
                        JOptionPane.showMessageDialog(this, "Se actualizo TAXI");
                        this.cmbDisponibles.removeAllItems();
                        for (String patente : rd.disponibles()) {
                            this.cmbDisponibles.addItem(patente);
                        }
                    } else {
                        JOptionPane.showMessageDialog(this, "No se actualizo TAXI");
                    }
                } else {
                    JOptionPane.showMessageDialog(this, "No se guardo CARRERA");
                }
            } else {
                this.pnlGuardar.setVisible(false);
                this.pnlAddCliente.setVisible(true);
            }
        } catch (SQLException ex) {
            System.out.println(ex.getMessage());
        }
    }//GEN-LAST:event_btnIngresarActionPerformed

