    private void btnIngresarClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIngresarClienteActionPerformed
        RadioTaxiDAL rd = new RadioTaxiDAL();
        Cliente client = new Cliente();
        client.setNombre(this.txtNombre.getText());
        client.setApellido(this.txtApellido.getText());
        client.setRut(this.txtRutCliente.getText());
        try {
            if (rd.saveCliente(client)) {
                JOptionPane.showMessageDialog(this, "Se guardo CLIENTE");
                this.pnlAddCliente.setVisible(false);
                this.pnlGuardar.setVisible(true);
            } else {
                JOptionPane.showMessageDialog(this, "No se guardo CLIENTE");
            }
        } catch (SQLException ex) {
            System.out.println(ex.getMessage());
        }
    }//GEN-LAST:event_btnIngresarClienteActionPerformed


